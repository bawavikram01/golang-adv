// =============================================================================
// PART 5: Building a simple code analyzer
// =============================================================================

func analyzeCode(src string) {
	fmt.Println("\n=== Code Analysis ===")

	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, "analyze.go", src, parser.ParseComments)
	if err != nil {
		fmt.Printf("Parse error: %v\n", err)
		return
	}

	var issues []string

	ast.Inspect(file, func(n ast.Node) bool {
		switch x := n.(type) {
		// Check for empty function bodies
		case *ast.FuncDecl:
			if x.Body != nil && len(x.Body.List) == 0 {
				pos := fset.Position(x.Pos())
				issues = append(issues,
					fmt.Sprintf("%s:%d: function %s has empty body",
						pos.Filename, pos.Line, x.Name.Name))
			}

		// Check for naked returns in functions with named results
		case *ast.ReturnStmt:
			if len(x.Results) == 0 {
				pos := fset.Position(x.Pos())
				issues = append(issues,
					fmt.Sprintf("%s:%d: naked return (can reduce readability)",
						pos.Filename, pos.Line))
			}

		// Check for TODO comments
		case *ast.Comment:
			if strings.Contains(x.Text, "TODO") || strings.Contains(x.Text, "FIXME") {
				pos := fset.Position(x.Pos())
				issues = append(issues,
					fmt.Sprintf("%s:%d: %s",
						pos.Filename, pos.Line, strings.TrimSpace(x.Text)))
			}
		}
		return true
	})

	if len(issues) == 0 {
		fmt.Println("No issues found!")
	} else {
		fmt.Printf("Found %d issues:\n", len(issues))
		for _, issue := range issues {
			fmt.Printf("  %s\n", issue)
		}
	}
}

