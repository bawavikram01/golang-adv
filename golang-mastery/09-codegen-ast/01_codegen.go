// =============================================================================

func demonstrateTransformation() {
	fmt.Println("\n=== AST Transformation ===")

	src := `
package example

func add(a, b int) int {
	return a + b
}

func multiply(a, b int) int {
	return a * b
}
`

	fset := token.NewFileSet()
	file, _ := parser.ParseFile(fset, "example.go", src, parser.ParseComments)

	// Transform: Add logging to every function
	ast.Inspect(file, func(n ast.Node) bool {
		fn, ok := n.(*ast.FuncDecl)
		if !ok {
			return true
		}

		// Create a log statement: fmt.Println("entering <funcname>")
		logStmt := &ast.ExprStmt{
			X: &ast.CallExpr{
				Fun: &ast.SelectorExpr{
					X:   ast.NewIdent("fmt"),
					Sel: ast.NewIdent("Println"),
				},
				Args: []ast.Expr{
					&ast.BasicLit{
						Kind:  token.STRING,
						Value: fmt.Sprintf(`"entering %s"`, fn.Name.Name),
					},
				},
			},
		}

		// Prepend the log statement to the function body
		fn.Body.List = append([]ast.Stmt{logStmt}, fn.Body.List...)
		return true
	})

	// Print the transformed code
	var buf bytes.Buffer
	format.Node(&buf, fset, file)
	fmt.Println("Transformed code:")
	fmt.Println(buf.String())
}

