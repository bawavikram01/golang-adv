// PART 1: Parsing Go Source Code
// =============================================================================

func demonstrateParsing() {
	fmt.Println("=== Parsing Go Source Code ===")

	src := `
package example

import "fmt"

// User represents a user in the system.
type User struct {
	ID    int64
	Name  string
	Email string
}

func (u *User) Greet() string {
	return fmt.Sprintf("Hello, %s!", u.Name)
}

func NewUser(name, email string) *User {
	return &User{Name: name, Email: email}
}
`

	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, "example.go", src, parser.ParseComments)
	if err != nil {
		fmt.Printf("Parse error: %v\n", err)
		return
	}

	// Walk the AST
	fmt.Printf("Package: %s\n", file.Name.Name)
	fmt.Printf("Imports: ")
	for _, imp := range file.Imports {
		fmt.Printf("%s ", imp.Path.Value)
	}
	fmt.Println()

	// Find all type declarations
	fmt.Println("\nType declarations:")
	ast.Inspect(file, func(n ast.Node) bool {
		switch x := n.(type) {
		case *ast.TypeSpec:
			fmt.Printf("  type %s\n", x.Name.Name)
			if st, ok := x.Type.(*ast.StructType); ok {
				for _, field := range st.Fields.List {
					for _, name := range field.Names {
						fmt.Printf("    field: %s %s\n", name.Name, formatNode(fset, field.Type))
					}
				}
			}
		case *ast.FuncDecl:
			recv := ""
			if x.Recv != nil && len(x.Recv.List) > 0 {
				recv = fmt.Sprintf("(%s) ", formatNode(fset, x.Recv.List[0].Type))
			}
			fmt.Printf("  func %s%s(", recv, x.Name.Name)
			if x.Type.Params != nil {
				params := []string{}
				for _, p := range x.Type.Params.List {
					for _, name := range p.Names {
						params = append(params, fmt.Sprintf("%s %s", name.Name, formatNode(fset, p.Type)))
					}
				}
				fmt.Print(strings.Join(params, ", "))
			}
			fmt.Print(")")
			if x.Type.Results != nil {
				fmt.Printf(" %s", formatNode(fset, x.Type.Results))
			}
			fmt.Println()
		}
		return true
	})
}

func formatNode(fset *token.FileSet, node ast.Node) string {
	var buf bytes.Buffer
	format.Node(&buf, fset, node)
	return buf.String()
}

