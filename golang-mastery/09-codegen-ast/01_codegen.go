const enumTemplate = `// Code generated by enum-gen; DO NOT EDIT.

package {{.Package}}

import "fmt"

type {{.TypeName}} int

const (
{{- range $i, $v := .Values}}
	{{$.TypeName}}{{$v}} {{$.TypeName}} = {{$i}}
{{- end}}
)

var _{{.TypeName}}Names = map[{{.TypeName}}]string{
{{- range $i, $v := .Values}}
	{{$.TypeName}}{{$v}}: "{{$v}}",
{{- end}}
}

func (e {{.TypeName}}) String() string {
	if name, ok := _{{.TypeName}}Names[e]; ok {
		return name
	}
	return fmt.Sprintf("{{.TypeName}}(%d)", int(e))
}

func Parse{{.TypeName}}(s string) ({{.TypeName}}, error) {
	for k, v := range _{{.TypeName}}Names {
		if v == s {
			return k, nil
		}
	}
	return 0, fmt.Errorf("invalid {{.TypeName}}: %q", s)
}

func (e {{.TypeName}}) IsValid() bool {
	_, ok := _{{.TypeName}}Names[e]
	return ok
}
`

type EnumDef struct {
	Package  string
	TypeName string
	Values   []string
}

func demonstrateCodeGen() {
	fmt.Println("\n=== Code Generation with Templates ===")

	tmpl, err := template.New("enum").Parse(enumTemplate)
	if err != nil {
		fmt.Printf("Template error: %v\n", err)
		return
	}

	def := EnumDef{
		Package:  "status",
		TypeName: "OrderStatus",
		Values:   []string{"Pending", "Processing", "Shipped", "Delivered", "Cancelled"},
	}

	var buf bytes.Buffer
	if err := tmpl.Execute(&buf, def); err != nil {
		fmt.Printf("Execute error: %v\n", err)
		return
	}

	// Format the generated code
	formatted, err := format.Source(buf.Bytes())
	if err != nil {
		fmt.Printf("Format error: %v\n", err)
		fmt.Println(buf.String()) // print unformatted
		return
	}

	fmt.Println(string(formatted))
}

