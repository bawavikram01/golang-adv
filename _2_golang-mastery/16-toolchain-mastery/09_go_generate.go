func mockGeneration() {
	fmt.Println("--- MOCK GENERATION ---")
	// ─── Why mocks? ───
	// You have an interface (e.g., UserStore).
	// In tests, you don't want a real database.
	// Generate a mock that implements the interface.
	//
	// ─── mockgen (uber, current standard) ───
	// go install go.uber.org/mock/mockgen@latest
	//
	// Source mode (from source file):
	// //go:generate mockgen -source=store.go -destination=mock_store.go -package=mocks
	//
	// Reflect mode (from interface name):
	// //go:generate mockgen -destination=mock_store.go -package=mocks mymodule/store UserStore
	//
	// ─── Using the mock in tests ───
	// func TestGetUser(t *testing.T) {
	//     ctrl := gomock.NewController(t)
	//     mockStore := mocks.NewMockUserStore(ctrl)
	//
	//     mockStore.EXPECT().
	//         GetByID(gomock.Any(), int64(42)).
	//         Return(&User{Name: "alice"}, nil)
	//
	//     svc := NewUserService(mockStore)
	//     user, err := svc.GetUser(ctx, 42)
	//     assert.NoError(t, err)
	//     assert.Equal(t, "alice", user.Name)
	// }
	//
	// ─── Alternative: counterfeiter ───
	// go install github.com/maxbrunsfeld/counterfeiter/v6@latest
	// //go:generate counterfeiter -o fakes/fake_store.go . UserStore
	//
	// ─── Alternative: moq ───
	// go install github.com/matryer/moq@latest
	// //go:generate moq -out mock_store.go . UserStore
	// Generates simple struct-based mocks (no framework).
	fmt.Println("  mockgen -source=x.go → generate interface mocks")
	fmt.Println("  EXPECT().Method().Return() → set expectations")
	fmt.Println("  Alternatives: counterfeiter, moq")
	fmt.Println()
}

