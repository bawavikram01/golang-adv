// =============================================================================
// PART 1: go generate Basics
// =============================================================================
func generateBasics() {
	fmt.Println("--- go generate BASICS ---")
	// ─── What is go generate? ───
	// A command that scans Go source files for //go:generate directives
	// and runs the specified commands.
	//
	// It does NOT run during build/test. You run it manually.
	// The generated code is committed to version control.
	//
	// ─── Syntax ───
	// //go:generate command arg1 arg2
	//
	// Must be at the start of the line (after //).
	// No space between // and go:generate.
	//
	// ─── Running ───
	// go generate              # process current package
	// go generate ./...        # process all packages
	// go generate -v ./...     # verbose (show commands)
	// go generate -n ./...     # dry run (show commands without running)
	// go generate -run "stringer" ./...  # only run matching generators
	//
	// ─── How it works ───
	// 1. Scans .go files for //go:generate comments
	// 2. Runs each command in order
	// 3. Commands generate .go files
	// 4. You commit the generated files
	//
	// ─── Special variables ───
	// //go:generate echo $GOFILE    # current filename
	// //go:generate echo $GOPACKAGE # current package name
	// //go:generate echo $GOLINE    # line number of directive
	// //go:generate echo $GOARCH    # target architecture
	// //go:generate echo $GOOS      # target OS
	// //go:generate echo $DOLLAR    # literal $ character
	//
	// ─── Convention ───
	// Generated files should start with:
	// // Code generated by <tool>; DO NOT EDIT.
	// This comment tells editors and tools to skip the file.
	fmt.Println("  //go:generate command args → embed generate commands")
	fmt.Println("  go generate ./... → run all generators")
	fmt.Println("  Generated files → commit to VCS")
	fmt.Println("  Convention: // Code generated by ...; DO NOT EDIT.")
	fmt.Println()
}

