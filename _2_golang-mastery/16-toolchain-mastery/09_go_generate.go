// PART 3: stringer Tool — Deep Dive
// =============================================================================
func stringerTool() {
	fmt.Println("--- stringer ---")
	// ─── Problem ───
	// type Day int
	// const (
	//     Sunday Day = iota
	//     Monday
	//     Tuesday
	// )
	// fmt.Println(Sunday) // prints: 0 (not useful!)
	//
	// ─── Solution: generate String() method ───
	// //go:generate stringer -type=Day
	//
	// This creates day_string.go with:
	// func (d Day) String() string {
	//     // returns "Sunday", "Monday", etc.
	// }
	// Now: fmt.Println(Sunday) // prints: Sunday
	//
	// ─── Flags ───
	// stringer -type=Day              # basic generation
	// stringer -type=Day,Color        # multiple types
	// stringer -type=Day -output=day_string.go  # custom filename
	// stringer -type=Day -linecomment # use line comments as names
	//
	// ─── Line comments ───
	// const (
	//     Sunday Day = iota // Sun
	//     Monday            // Mon
	//     Tuesday           // Tue
	// )
	// //go:generate stringer -type=Day -linecomment
	// Now: Sunday.String() == "Sun"
	//
	// ─── Full example ───
	// // file: status.go
	// package order
	//
	// //go:generate stringer -type=Status
	//
	// type Status int
	// const (
	//     Pending  Status = iota  // 0
	//     Active                  // 1
	//     Complete                // 2
	//     Cancelled               // 3
	// )
	//
	// Run: go generate ./...
	// Creates: status_string.go (auto-generated, don't edit)
	fmt.Println("  stringer -type=Enum → generate String() method")
	fmt.Println("  -linecomment → use comments as display names")
	fmt.Println("  Prints enum names instead of numbers")
	fmt.Println()
}

