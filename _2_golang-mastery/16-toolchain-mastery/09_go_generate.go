// PART 5: Protocol Buffer Generation
// =============================================================================
func protoGeneration() {
	fmt.Println("--- PROTOBUF GENERATION ---")
	// ─── Install tools ───
	// # protoc compiler (language-independent)
	// apt install protobuf-compiler  # or brew install protobuf
	//
	// # Go plugins
	// go install google.golang.org/protobuf/cmd/protoc-gen-go@latest
	// go install google.golang.org/grpc/cmd/protoc-gen-go-grpc@latest
	//
	// ─── Proto file ───
	// // proto/user.proto
	// syntax = "proto3";
	// package user;
	// option go_package = "myapp/gen/userpb";
	//
	// service UserService {
	//     rpc GetUser(GetUserRequest) returns (GetUserResponse);
	// }
	// message GetUserRequest {
	//     int64 id = 1;
	// }
	// message GetUserResponse {
	//     string name = 1;
	//     string email = 2;
	// }
	//
	// ─── Generate directive ───
	// //go:generate protoc --go_out=. --go_opt=paths=source_relative \
	// //   --go-grpc_out=. --go-grpc_opt=paths=source_relative \
	// //   proto/user.proto
	//
	// Generates:
	// - user.pb.go (message types)
	// - user_grpc.pb.go (gRPC client/server stubs)
	//
	// ─── buf: modern protobuf toolchain ───
	// Better than raw protoc. Handles dependencies, linting, formatting.
	// # Install: https://buf.build/docs/installation
	// buf generate    # replaces protoc commands
	// buf lint        # lint proto files
	// buf breaking    # detect breaking changes
	fmt.Println("  protoc --go_out → generate Go from .proto")
	fmt.Println("  protoc-gen-go-grpc → generate gRPC stubs")
	fmt.Println("  buf → modern protobuf toolchain (recommended)")
	fmt.Println()
}

