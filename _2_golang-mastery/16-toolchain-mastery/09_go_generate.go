// PART 6: //go:embed — Embed Files at Compile Time
// =============================================================================
func embedDirective() {
	fmt.Println("--- go:embed ---")
	// ─── Embed files into your Go binary (Go 1.16+) ───
	// import "embed"
	//
	// //go:embed config.json
	// var configData []byte
	//
	// //go:embed template.html
	// var templateStr string
	//
	// //go:embed static/*
	// var staticFiles embed.FS
	//
	// ─── Rules ───
	// - Must be package-level variable
	// - Variable type: string, []byte, or embed.FS
	// - Paths are relative to the source file
	// - Directories embedded recursively
	// - Files starting with . or _ excluded by default
	//   Use //go:embed all:static to include them
	//
	// ─── embed.FS usage ───
	// //go:embed templates/*
	// var templates embed.FS
	//
	// // Use with http.FileServer:
	// http.Handle("/static/", http.FileServer(http.FS(staticFiles)))
	//
	// // Use with template.ParseFS:
	// tmpl, _ := template.ParseFS(templates, "templates/*.html")
	//
	// // Read specific file:
	// data, _ := templates.ReadFile("templates/index.html")
	//
	// ─── Use cases ───
	// - Embed SQL migration files
	// - Embed HTML/CSS/JS for web servers (single binary deploy!)
	// - Embed config defaults
	// - Embed version info
	// - Embed CA certificates
	//
	// ─── NOT go generate ───
	// embed is a compiler directive, not go generate.
	// It runs at build time automatically.
	// No //go:generate needed.
	fmt.Println("  //go:embed file.txt → embed file in binary")
	fmt.Println("  embed.FS → embed whole directories")
	fmt.Println("  Single binary with all static files!")
	fmt.Println("  Not go:generate — runs at build time")
	fmt.Println()
}

